    private void crear(){
        try{
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("dd/MM/yyyy");
            Date dt =null;
            try{
            dt = formatoDelTexto.parse(fecha.getText());
            Nodo temp=new Nodo(dt,Long.parseLong(cta.getText()),Float.parseFloat(monto.getText()),true);
                if(root!=null){
                  Nodo aux=root;
                  moverse(aux,temp);
                }else{
                 end=root=temp;
                 JOptionPane.showMessageDialog(null, "Cuenta Creada");
                }
            }catch (ParseException ex) {
                
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "numero de cuenta debe ser numerico");
        }
        
}
    public void mostrar(SimpleDateFormat ft){
        Nodo aux=root;
       while(aux!=null){
           System.out.println(aux.getNro()+" "+ ft.format(aux.getFecha())+" "+aux.getMonto());
           aux=aux.getRl();
       }
       if(root!=null && end!=null)System.out.println("root "+root.getNro()+" end "+end.getNro());
    } 
    private void moverse(Nodo temp,Nodo add){
        if(temp.getRl()==null ){
            if(temp.getNro()==add.getNro()){
            JOptionPane.showMessageDialog(null, "Esta nro de cuenta ya existe");
            }else if(add.getNro()<temp.getNro()){
                add.setRl(temp);
                JOptionPane.showMessageDialog(null, "Cuenta Creada");
                root=add;
            }else{
                temp.setRl(add);
                JOptionPane.showMessageDialog(null, "Cuenta Creada");
                end=temp=add;
            }
        }else if(temp.getNro()==add.getNro()){
            JOptionPane.showMessageDialog(null, "Esta nro de cuenta ya existe");
        }else if(add.getNro()<temp.getNro()){
                add.setRl(temp);
                JOptionPane.showMessageDialog(null, "Cuenta Creada");
                root=add;
        }else{
            moverse(temp.getRl(),add);
        }        
    }
    
