    private void buscar(long num){
        Nodo temp=root;
        float sum=0f,res=0f;
        boolean b=true;
        while(temp!=null){
            if(temp.getNro()==num){
                if(temp.isB())sum+=temp.getMonto();
                else res+=temp.getMonto();
                b=false;
            }
            temp=temp.getRl();
        }
        if(b)JOptionPane.showMessageDialog(null, "La cuenta no existe");
        else JOptionPane.showMessageDialog(null, "El saldo de la cuenta "+num+" es de "+(sum-res));  
    }
